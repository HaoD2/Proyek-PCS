--MASTER PELAJARAN
CREATE TABLE PELAJARAN (
	ID INT PRIMARY KEY,
	NM_MAPEL VARCHAR2(255) NOT NULL,
    KODE VARCHAR2(50) NOT NULL
);

CREATE TABLE JURUSAN (
	ID INT PRIMARY KEY,
	NM_JUR VARCHAR2(50) NOT NULL,
	KODE_JUR VARCHAR2(50) NOT NULL
);
--MASTER USER
CREATE TABLE SISWA(
    NISN VARCHAR2(12) PRIMARY KEY NOT NULL,
    NIS VARCHAR2(7) NOT NULL UNIQUE,
    NAMA VARCHAR2(255) NOT NULL,
    JENIS_KELAMIN VARCHAR2(1) CHECK(JENIS_KELAMIN = 'L' OR JENIS_KELAMIN = 'P'),
    TANGGAL_LAHIR DATE NOT NULL,
    PASS VARCHAR2(255) NOT NULL,
    ALAMAT VARCHAR2(255) NOT NULL,
    NOTELP VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2(255) NOT NULL,
    NAMA_ORTU VARCHAR(255) NOT NULL,
    ID_JURUSAN NUMBER(1),
    CONSTRAINT PK_IDJUR FOREIGN KEY (ID_JURUSAN) REFERENCES JURUSAN(ID)
);

CREATE TABLE GURU(
    KODE_GURU VARCHAR2(255) PRIMARY KEY,
    NAMA VARCHAR2(255) NOT NULL,
    JENIS_KELAMIN VARCHAR2(1) CHECK(JENIS_KELAMIN = 'L' OR JENIS_KELAMIN = 'P') NOT NULL,
    MENGAJAR VARCHAR2(255) NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    NOTELP VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2(255) NOT NULL,
    ALAMAT VARCHAR2(255)
);

--MASTER NILAI

CREATE TABLE NILAI_IPA(
    ID NUMBER PRIMARY KEY,
	ROW_NISN REFERENCES SISWA(NISN) NOT NULL,
	NILAI_BIOLOGI NUMBER,
	NILAI_FISIKA NUMBER,
	NILAI_KIMIA NUMBER,
	NILAI_MATEMATIKA_MINAT NUMBER
);

CREATE TABLE NILAI_IPS(
    ID NUMBER PRIMARY KEY,
	ROW_NISN REFERENCES SISWA(NISN) NOT NULL,
	NILAI_GEOGRAFI NUMBER,
	NILAI_SOSIOLOGI NUMBER,
	NILAI_EKONOMI NUMBER,
	NILAI_SEJARAH NUMBER 
);

CREATE TABLE NILAI_UMUM(
    ID NUMBER PRIMARY KEY,
	ROW_NISN REFERENCES SISWA(NISN),
	NILAI_PENJASKES NUMBER,
	NILAI_BAHASA_INGGRIS NUMBER,
	NILAI_BAHASA_INDONESIA NUMBER,
	NILAI_PKN NUMBER,
	NILAI_SENI_BUDAYA NUMBER 
);

--MASTER MINAT BAKAT
CREATE TABLE EKSTRA(
    KODE_EKSTRA VARCHAR(20) PRIMARY KEY,
	NAMA_EKSTRA VARCHAR2(255) NOT NULL
);

CREATE TABLE D_EKSTRA(
    ROW_NIS REFERENCES SISWA(NIS) NOT NULL,
    ROW_KODE_EKSTRA REFERENCES EKSTRA(KODE_EKSTRA) NOT NULL
);

--MASTER KELAS
CREATE TABLE KELAS(
    KODE_KELAS VARCHAR2(50) PRIMARY KEY,
    NAMA_KELAS VARCHAR2(50) NOT NULL
);

CREATE TABLE D_KELAS(
    ROW_KODE_KELAS REFERENCES KELAS(KODE_KELAS) NOT NULL,
    ROW_NIS REFERENCES SISWA(NIS) NOT NULL
);

